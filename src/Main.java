    private void bt_ingresareMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bt_ingresareMouseClicked
        String userr = tf_usuarioLogin.getText();
        String pass = tf_passLogin.getText();
        adminBandas ap = new adminBandas("./bandas.cbm");
        ap.cargarArchivoBandas();
        adminSolistas ss = new adminSolistas("./solistas.cbm");
        ss.cargarArchivoSolistas();
        for (int i = 0; i < ap.getBands().size(); i++) {
            if (ap.getBands().get(i).getUsuario().equals(userr) && ap.getBands().get(i).getContrase単a().equals(pass)) {
                JOptionPane.showMessageDialog(this, " Es una banda");
            }

        }
        for (int i = 0; i < ss.getSolis().size(); i++) {

            if (ss.getSolis().get(i).getUsuario().equals(userr) && ss.getSolis().get(i).getContrase単a().equals(pass)) {
                JOptionPane.showMessageDialog(this, " Es un solista");
            }
        }
    

    }//GEN-LAST:event_bt_ingresareMouseClicked

